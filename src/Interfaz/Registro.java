    public void sumaPrecios() {
        //AquÃ­ sumo los valores de la fila para colocarlos en el JTextField totalCampo
        int totalRow = jtabla.getRowCount();
        totalRow -= 1;
        for (int i = 0; i <= (totalRow); i++) {
            sumatoria = Integer.parseInt(String.valueOf(jtabla.getValueAt(i, 5)));
            sumatoria1 += sumatoria;
        }
        jtotalCampo1.setText(String.valueOf(sumatoria1));
        
    }
    
